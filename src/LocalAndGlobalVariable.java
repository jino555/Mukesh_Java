
public class LocalAndGlobalVariable {
	String s;
	float f;
	long l;
	byte b;
	char c;
		int i;
		boolean bo;
		double d;
	public static void main(String[] args) {
		
		LocalAndGlobalVariable obj2 = new LocalAndGlobalVariable();
		
		System.out.println("s "+obj2.s);
		System.out.println("f "+obj2.f);
		System.out.println("l "+obj2.l);
		System.out.println("b "+obj2.b);
		System.out.println("c "+obj2.c);
		System.out.println("i "+obj2.i);
		System.out.println("boo "+obj2.bo);
		System.out.println("d "+obj2.d);

	}

}
